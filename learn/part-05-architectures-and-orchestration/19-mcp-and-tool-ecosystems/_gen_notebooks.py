"""Generator for the Ch 19 companion notebooks (19-01, 19-02).

Run once to (re)produce the .ipynb files in this folder. This keeps the JSON
escaping correct and guarantees nbformat-4 compliance (empty outputs,
execution_count null). It is a build helper, not part of the course.
"""
import json
import os

HERE = os.path.dirname(os.path.abspath(__file__))


def md(*lines):
    return {"cell_type": "markdown", "metadata": {}, "source": _src(lines)}


def code(*lines):
    return {
        "cell_type": "code",
        "execution_count": None,
        "metadata": {},
        "outputs": [],
        "source": _src(lines),
    }


def _src(lines):
    """Turn a tuple of logical lines into nbformat source: each element keeps
    its trailing newline except the last."""
    flat = []
    for block in lines:
        flat.extend(block.split("\n"))
    out = []
    for i, ln in enumerate(flat):
        out.append(ln + ("\n" if i < len(flat) - 1 else ""))
    return out


def notebook(cells):
    return {
        "cells": cells,
        "metadata": {
            "kernelspec": {
                "display_name": "Python 3",
                "language": "python",
                "name": "python3",
            },
            "language_info": {"name": "python"},
        },
        "nbformat": 4,
        "nbformat_minor": 5,
    }


def write(name, nb):
    path = os.path.join(HERE, name)
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        json.dump(nb, f, indent=1, ensure_ascii=False)
        f.write("\n")
    return path


